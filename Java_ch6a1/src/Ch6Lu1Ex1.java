/*Create an array of integers. Store five different values in it. Print the 
 contents of the array using a "for" loop. */

/** @author KAILASH JOSHI*/
public class Ch6Lu1Ex1 {
	public static void main(String[] args) {
		int int_array[] = { 5, 10, 15, 20, 25 };

		for (int i = 0; i < int_array.length; i++) {
			System.out.println("The " + i + " element of the vector is "
					+ int_array[i]);
		}
	}
}

