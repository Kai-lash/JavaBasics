public class  Ch4Lu2Ex2
{
	public static void main(String args[])
	{
		int ivar1=3;
		float fvar1=2.25f;
		
		
		//Cubing fvar1
		fvar1=fvar1 * fvar1 * fvar1;
	
		//Displaying the result of the cube
		System.out.println("Cube of 2.25 :- "+fvar1);
			
		//Squaring ivar1
		ivar1=ivar1*ivar1;
			
		//Displaying the result of the square
		System.out.println("Output :- "+ivar1);
			
		//Addition of int and float variables
		fvar1=fvar1+ivar1;
			
		//Displaying the result after addition with int variable
		System.out.println("Output :- "+fvar1);
	}
}


