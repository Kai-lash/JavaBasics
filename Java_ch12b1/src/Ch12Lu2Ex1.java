/*Write a program to check for even numbers 
 * using a for loop until 20.
 * Set breakpoint at appropriate line and check each 
 * step result in the debug mode. 
 */

/** @author KAILASH JOSHI*/
public class Ch12Lu2Ex1 {

	public static void main(String[] args) {
		for (int i = 0; i <= 20; i++) {
			if (i % 2 == 0) {
				System.out.println(i); // Breakpoint
			}
		}
	}
}
