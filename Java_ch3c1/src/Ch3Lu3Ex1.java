/* Initialize an integer variable to the value 10. Increment the value using the post increment operator. Print the value before and after applying the post increment operator. Now apply the pre-increment operator. Print the value of the variable on the console again.*/

/** @author KAILASH JOSHI*/

public class Ch3Lu3Ex1
 {
  	public static void main (String[] args)
	 {
  		int int_var=10;
  		System.out.println("The value of the integer variable initially : "+int_var);
  		int_var++;
  		System.out.println("The value of the integer variable after applying post increment operator : "+int_var);
  		++int_var;
  		System.out.println("The value of the integer variable after applying pre increment operator : "+int_var);
	 }
 }

